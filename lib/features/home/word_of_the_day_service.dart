  /// Отримує слово дня, прив'язане до поточної дати
  static WordOfTheDay getTodayWord() {
    final now = DateTime.now();
    // Рахуємо кількість днів від умовної епохи
    final daysSinceEpoch = now.difference(DateTime(2024, 1, 1)).inDays;
    
    // Беремо остачу від ділення, щоб циклити список
    final index = daysSinceEpoch % _pool.length;
    return _pool[index];
  }
